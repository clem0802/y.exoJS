/*---------------(WHILE LOOP)---------------*/
// Create a loop that runs as long as i is less than 10, 
// but increase i with 2 each time.
let i = 0;
while (i < 10){
    console.log(i);
    i = i + 2;
}
// 0
// 2
// 4
// 6
// 8
// 10